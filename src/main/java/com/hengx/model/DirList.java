package com.hengx.model;

public class DirList {

    private int id;
    private int urlid;
    private String dirurl;
    private int status;


    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getUrlid() {
        return urlid;
    }

    public void setUrlid(int urlid) {
        this.urlid = urlid;
    }

    public String getDirurl() {
        return dirurl;
    }

    public void setDirurl(String dirurl) {
        this.dirurl = dirurl;
    }

    public int getStatus() {
        return status;
    }

    public void setStatus(int status) {
        this.status = status;
    }
}
